    <h1>Google custom search result subscription</h1>
    <p><strong>This feature is no longer available because Google
      has <a href="http://searchengineland.com/google-shutters-fast-flip-sidewiki-aardvark-subscribed-links-91554">dropped its Subscribed Links
      feature</a>.</strong></p>
    <p><strike>If you use Google, then you can subscribe to custom results from
      prefix.cc. This means that a search for “foaf” will prominently show
      the FOAF namespace from prefix.cc among your usual search
      results.</strike></p>
    <p><strike>You need to be logged into your Google Account for this to
      work.</strike></p>
